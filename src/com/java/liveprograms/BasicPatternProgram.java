package com.java.liveprograms;

public class BasicPatternProgram {

	public static void main(String[] args) {

		/**
		 * 
		 * 
		 * 1 2 3 4
		 * 
		 * control+shift+/
		 * 
		 * @param args
		 */

		for (int i = 0; i < 4; i++) {

			for (int j = 0; j < 4; j++) {
				System.out.print(i);
				//System.out.print(j);
			}
			System.out.println();
		}

	}

}
